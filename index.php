<?php
/**
 * 前端控制器 - 整个应用的统一入口点
 * 
 * 这是路由系统的入口文件，所有请求都会经过这个文件
 * 
 * 工作流程：
 * 1. Apache的.htaccess将所有"虚拟"URL重定向到这里
 * 2. 引入必要的文件（预处理库、路由器）
 * 3. 创建路由器实例
 * 4. 调用路由器的dispatch()方法分发请求
 * 5. 路由器根据URL找到对应的控制器并执行
 * 
 * 这种模式的优点：
 * - 统一入口，便于安全控制和日志记录
 * - URL美化，支持友好的URL格式
 * - 灵活的路由配置，便于扩展
 */

// 引入预处理库 - 提供基础功能和错误处理
include_once __DIR__ . '/include/_PRE.php';

// 引入路由器 - 负责URL解析和请求分发
include_once __DIR__ . '/include/router.php';

// 创建路由器实例
$router = new Router();

// 分发请求 - 路由器会自动解析URL并调用对应的控制器方法
$router->dispatch();
?>